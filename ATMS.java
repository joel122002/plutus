    private void setScreen() {
        switch (applicationState) {
            case LOGGED_OUT:
                showLoginScreen();
                disableAllSideButtons();
                break;
            case LOGGED_IN_MAIN_MENU:
                showMainMenu();
                enableAllSideButtons();
                break;
            case BALANCE:
                showBalance();
                disableAllSideButtons();
                break;
            case CASH:
                showWithdrawCash();
                disableAllSideButtons();
                break;
            case BALANCE_RECEIPT:
                break;
            case CHANGE_PIN:
                showNewPin();
                disableAllSideButtons();
                break;
            case FAST_CASH:
                break;
        }
    }

