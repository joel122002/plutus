    private void setScreen() {
        switch (applicationState) {
            case CARD_NUMBER:
                showLoginCardNumber();
                disableAllSideButtons();
                break;
            case ENTER_PIN:
                showLoginScreen();
                disableAllSideButtons();
                break;
            case LOGGED_IN_MAIN_MENU:
                showMainMenu();
                enableAllSideButtons();
                jBtnRightPanel3.setEnabled(false);
                break;
            case BALANCE:
                showBalance();
                disableAllSideButtons();
                break;
            case CASH:
                showWithdrawCash();
                disableAllSideButtons();
                break;
            case BALANCE_RECEIPT:
                showBalanceReceipt();
                disableAllSideButtons();
                break;
            case CHANGE_PIN:
                showNewPin();
                disableAllSideButtons();
                break;
            case FAST_CASH:
                showFastCash();
                enableAllSideButtons();
                break;
        }
    }

